// Función para obtener artistas de un país
export const getArtistasPorPais = (paisId, tipo = 'todos') => {
  const pais = ESCENAS_DATA[paisId];
  if (!pais) return [];
  
  if (tipo === 'establecidos') return pais.artistas.establecidos;
  if (tipo === 'emergentes') return pais.artistas.emergentes;
  if (tipo === 'mujeres') return pais.artistas.mujeres;
  return [...pais.artistas.establecidos, ...pais.artistas.emergentes];
};